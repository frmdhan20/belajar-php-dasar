<?php 
// Pengulangan
// for
// while
// do.. while
// foreach : pengulangan khusus array

// for
for( $i = 0; $i < 5; $i++ ) { 
	echo "(1. Pengulangan for) Hello World!<br>";
}
echo "<hr>";

// while
$i = 0;
while ($i < 5) {
	echo "(2. while) AMIK Taruna Probolinggo<br>";
$i++;
}
echo "<hr>";

// do.. while
$i = 0;
do {
	echo "(3. do.. while) Fajar Ramadhan<br>";

$i++;
} while ( $i < 5 );
echo "<hr>";

 ?>